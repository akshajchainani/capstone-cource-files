class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def insert_at_beginning(self, data):
        new_node = Node(data)
        new_node.next = self.head
        self.head = new_node

    def insert_at_end(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
        else:
            current = self.head
            while current.next:
                current = current.next
            current.next = new_node

    def display(self):
        current = self.head
        while current:
            print(current.data, end="  ")
            current = current.next
        print("None")

linkedlist = LinkedList()

print("Enter 5 values to be inserted at the beginning:")
for _ in range(5):
    data = int(input())
    linkedlist.insert_at_beginning(data)

print("LinkedList:")
linkedlist.display()
